package Model;

/**
 *
 * @author Danel
 */
public class Estado implements Jsonable{
    int codigo;
    String nombre;
    String descripcion;

    public Estado() {
        this.codigo = -1;
        this.nombre = "";
        this.descripcion = "";
    }

    public Estado(int codigo, String nombre, String descripcion) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.descripcion = descripcion;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

}
